#include "libft.h"

int			ft_sqrt(int x)
{
	int		y;

	y = 2;
	if (x < 0)
		return (-1);
	if ((x == 0) || (x == 1))
		return (x);
	while (y * y <= x)
	{
		if ((y * y) == x)
			return (y);
		y++;
	}
	return (-1);
}
