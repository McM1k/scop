#include "scop.h"

t_mat	rev_s(t_mat s)
{
	s.col[0].x = 1 / s.col[0].x;
	s.col[1].y = 1 / s.col[1].y;
	s.col[2].z = 1 / s.col[2].z;
	return (s);
}

t_mat	rev_t(t_mat t)
{
	t.col[3].x *= -1;
	t.col[3].y *= -1;
	t.col[3].z *= -1;
	return (t);
}
